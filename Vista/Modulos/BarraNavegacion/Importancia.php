    <blockquote  class="descripcion"><P class="parrafo" align="justify" >
        <br> Una de las herramientas que los docentes tienen para lograr que los alumnos se apropien de un aprendizaje significativo, son los proyectos de aula, de aprendizaje o ahora los proyectos productivos. Estos permiten al docente y alumnos, solucionar situaciones dentro y fuera del aula y al mismo tiempo desarrollan habilidades cognitivas que facilita el aprendizaje de los contenidos básico del semestre.<br><br>
        Los proyectos permiten al docente globalizar o integrar las áreas académicas a través de una temática especifica escogida por los alumnos según sus intereses y necesidades.<br><br>
        Una herramienta de aprendizaje tiene la ventajas de organizar los contenidos con los cuales el docente puede trabajar según el tema y al mismo tiempo desarrolla el entusiasmo por la investigación  a los alumnos como al docente.<br><br>
        Durante el semestre academico se pueden realizar varios proyectos cortos según la época, o situaciones relevantes que sean del interés del alumno y docente.<br><br>
        Es importante resaltar que los proyectos largos son tediosos y aburridos, de ahí que cuando se hacen cortos, los alumnos nunca pierden el entusiasmo.<br><br>
        Una de las características de los proyectos de aula es que son innumerables las actividades practicas que se pueden hacer de todas las áreas académicas.<br><br>
        En la actualidad se deben promover el desarrollo de proyectos productivos a partir de las necesidades de la Universidad y comunidad, en los cuales se involucren todos los integrantes de la comunidad educativa. Durante muchos años la práctica pedagógica ha sido con proyectos, mejorando considerablemente la comprension de los temas por parte de los estudiantes.<br><br>   
        </P></blockquote>
    
