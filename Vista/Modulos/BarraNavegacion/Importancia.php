        <blockquote class="descripcion"><ul class="ventajas" align="justify" >
        <li>• Se integran los contenidos programativos.</li>
        <li>• Es una herramienta de apoyo en el desasarrollo de los contenidos.</li>
        <li>• Se consibe el conocimiento como un todo ..no como areas academicas.</li>
        <li>• Se seleccionaran los contenidos de acuerdo a la tematica escogida para el proyecto.</li>
        <li>• Los alumnos construyen su textos significativos basandose en los contenidos programaticos.</li>
