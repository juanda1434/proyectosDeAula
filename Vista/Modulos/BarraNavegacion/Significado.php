    <p class="l">
         Este proyecto se realizó el pasado 15 de Junio de 2017, la cual fue la Primera Feria de Proyectos de Aula de la carrera Ingeniería de Sistemas de la Universidad Francisco de Paula Santander, la cual fue  organizada por los docentes Carlos René Angarita Sanguino y Claudia Yamile Gómez Llanez, esta actividad académica se realizó en el pasillo de cuarto piso del edificio Aula Sur desde las 8:00 am hasta las 12:00 m. En este espacio se inscribieron 21 proyectos de aula de los que se presentaron 19 proyectos, distribuidos así, 9 equipos de la asignatura Seminario de Integrador III, 1 equipo de la asignatura Gestión de Base de Datos Avanzada, 4 equipos de la asignatura de Bases de Datos y 5 equipos de la asignatura de Programación Web, para un total de 44 participantes como ponentes.
        <br><br>La actividad contó con ocho (8) evaluadores, quienes se tomaron la tarea de revisar en los respectivos puestos asignados para evaluar que los proyectos de aula cumplieran con los criterios definidos y establecidos en un formato diseñado para ello.
        <br><br> Al terminar la feria se tomaron en cuenta los mejores resultados entregados por los pares evaluadores y se realizó la premiación del evento a los seis (6) primeros puestos. Esta premiación se dió gracias al patrocinio de empresas como: <b>DOCUXER, TKSIS LTDA, FERROELECTRICOS SAN VICENTE, CODETEIN, PARRA&SANCHEZ, THE POP ROCKS Y DISTRIBUIDORA DIFFER</b> .
