            <div class="col-md-12 text-justify"><br> <p class="parrafo"> Proyectos de aula es una actividad universitaria de la carrera de ingeniería de sistemas de la universidad francisco de paula Santander  que se enfoca en propiciar el desarrollo de las competencias e integrar las habilidades y conocimientos adquiridos en los estudiantes de las demás asignaturas a lo largo del semestre y de la carrera.
        <br>En el marco de las asignaturas de la carrera de Ingeniería de Sistemas guiadas en el semestre, los estudiantes de la carrera desarrollan proyectos de aula, en los cuales integran sus habilidades y conocimientos adquiridos en las demás asignaturas.</p>
            
